function [f] = accelws(x,P,mu,Mc,g,w)

N2=x(1);  r=x(2); t=x(3); 


v2=((r*((2*pi)./60))*((11./N2)*2500));

A=-(v2./t);
% Mt=((((P/((2*pi)./60))*((11./N2)*2500))./r)./(-A+(mu*g)));
% Mm= Mt - Mc;
Mm=((((P./(((2*pi)./60)*((11./N2)*2500)))./(r*sin(0.785398)))./(-A+(mu*g)))-Mc)/100;
f = w(1)*-A + w(2)*-Mm;

